### Operators 
# Addition, Subtraction, Multiplication, Division, Quotient, Remainder

x1 = 22
x2 = 11

x3 = x1 + x2
print(x3)

x4 = x1 - x2
print(x4)

x5 = x1 * x2
print(x5)

x6 = x1 / x2
print(x6, type(x6))

x7 = x1 // x2
print(x7)

x8 = x1 % x2
print(x8)

# Conditional statements

# Conditional statements are of two types : if, else
# If more conditions are applicable then we will use elif
# For 4 condition based scenario we will use : if, elif, elif, else

p = 12

if (p % 2 == 0):
    print("even number")

else:
    print("odd number")


